export const Card = ({ owner, src, name, price, onBuy }: CardProps) => {
  //@ts-ignore
  const provider = new ethers.providers.Web3Provider(window.ethereum);
  const contractAddress = "0x42437974Bc49D153a8F6a9C1519042554b430763";
  const contractObject = new ethers.Contract(contractAddress, ABI, provider);
  const signer = provider.getSigner();
  const contractWithSigner = contractObject.connect(signer);

  const buyCard = async () => {
    try {
      await contractWithSigner.createStorybook(["jhdbashfdbasfbahfb"]);
      // If the purchase is successful, trigger the onBuy callback
      if (onBuy) {
        onBuy();
      }
      toast.success("Bought Successfully");
    } catch (error) {
      toast.error("Failed to Buy");
    }
  };

  return (
    <div className="card explore-card">
      <figure className="card-banner">
        <a href="#">
          <img
            src={src}
            width={300}
            height={300}
            loading="lazy"
            alt="Walking On Air"
            className="img-cover"
          />
        </a>
      </figure>

      <h3 className="h3 card-title">
        <a href="#">{name}</a>
      </h3>

      <span className="card-author">
        Owned By{" "}
        <a href="#" className="author-link">
          {owner}
        </a>
      </span>

      <div className="wrapper">
        <data className="wrapper-item" value="1.5">
          {price} ETH
        </data>
        <button onClick={buyCard} className="btn">
          <span>Buy</span>
        </button>
      </div>
    </div>
  );
};
